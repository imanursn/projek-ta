    private void BtnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnTambahActionPerformed

        // MENDAPATKAN NILAI INPUT DARI FORM
        String nik = InputNik.getText();
        String nama = InputNama.getText();
        String divisi = (String) CmbDivisi.getSelectedItem();
        String jabatan = (String) CmbJabatan.getSelectedItem();
        
        //INPUT TGL 
        Date selectedDate = InputTgl.getCalendar().getTime();
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String tgl = sdf.format(selectedDate);
   
       
        //VALIDASI INPUT
        if (nik.isEmpty() || nama.isEmpty() || divisi.equals("PILIH DIVISI") || jabatan.equals("PILIH JABATAN") || tgl == null) {
            JOptionPane.showMessageDialog(null, "Harap Lengkapi Semua Data", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        // SIMPAN DI DATABASE
        try (Connection conn = db_connect.connect()) {
            String sql = "INSERT INTO karyawan (nik_karyawan, nama_karyawan, divisi_karyawan, jabatan_karyawan, tglmasuk_karyawan) VALUES (?, ?, ?, ?, ?)";
            PreparedStatement ppstmt = conn.prepareStatement(sql);
            ppstmt.setString(1, nik);
            ppstmt.setString(2, nama);
            ppstmt.setString(3, divisi);
            ppstmt.setString(4, jabatan);
            ppstmt.setString(5, tgl);
            ppstmt.executeUpdate();
            
            JOptionPane.showMessageDialog(null, "Data Karyawan Berhasil Ditambahkan", "Informasi", JOptionPane.INFORMATION_MESSAGE);
            
            // KOSONGKAN FORM
            InputNik.setText("");
            InputNama.setText("");
            CmbDivisi.setSelectedIndex(0);
            CmbJabatan.setSelectedIndex(0);
            InputTgl.setCalendar(null); 
            

            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data Karyawan Gagal Ditambahkan" + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }       
    }//GEN-LAST:event_BtnTambahActionPerformed

