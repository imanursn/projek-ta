    public void LoadDivisi(){
        try (Connection conn = db_connect.connect()){
            CmbDivisi.removeAllItems();
            CmbDivisi.addItem("PILIH DIVISI");
            String sql = "SELECT nama_divisi FROM divisi";
            Statement stmt = conn.createStatement();
            ResultSet rs = stmt.executeQuery(sql);
            while (rs.next()){
                CmbDivisi.addItem(rs.getString("nama_divisi"));
            } 
        }catch (Exception e){
            System.out.println("Combo Divisi Gagal Masuk" + e.getMessage());
        }
    }
    
